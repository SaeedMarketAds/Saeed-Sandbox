# السطر 1: استيراد المكتبات الأساسية لتشغيل الواجهة
import streamlit as st

# السطر 2: استيراد دالة البحث المحلية من مجلد المحرك الخاص بك
from engine.inference import search_knowledge_base

# السطر 3: إعدادات الصفحة الافتراضية للتطبيق (العنوان والأيقونة)
st.set_page_config(page_title="Saeed LogiC Pro", page_icon="🚀", layout="centered")

# السطر 4: عرض العنوان الرئيسي للمساعد في أعلى الواجهة
st.title("Saeed LogiC Pro 🚀")

# السطر 5: عرض الوصف الفرعي لمساعد التسوق الذكي
st.subheader("نظام محلي لدمج العروض والكوبونات")

# ----------------------------------------------------------
# السطر 6: تعريف قائمة التحيات الشائعة للتعرف عليها تلقائياً
greetings = ["السلام عليكم", "مرحبا", "أهلا", "هلا", "سلام", "مساء الخير", "صباح الخير"]
# ----------------------------------------------------------

# السطر 7: بناء شريط إدخال النص المخصص للمستخدم في أسفل الشاشة
user_input = st.chat_input("...اسألني عن العروض المتاحة")

# السطر 8: بدء معالجة البيانات بمجرد أن يكتب المستخدم سؤاله ويضغط إدخال
if user_input:
    
    # السطر 9: عرض رسالة المستخدم الحالية في محادثة الواجهة
    with st.chat_message("user"):
        st.write(user_input)
        
    # السطر 10: الفحص الذكي - هل النص المدخل عبارة عن تحية؟
    if any(greet in user_input.strip() for greet in greetings):
        # السطر 11: الرد الترحيبي السلس دون الحاجة لفتح قاعدة البيانات
        reply = "وعليكم السلام ورحمة الله وبركاته! أهلاً بك في مساعد التسوق الخاص بك. كيف يمكنني مساعدتك في العروض والكوبونات اليوم؟"
    
    else:
        # السطر 12: إذا لم تكن تحية، يتم توجيه السؤال مباشرة لدالة البحث في ملف الـ JSON
        reply = search_knowledge_base(user_input) 
        
    # السطر 13: عرض رد مساعد التسوق النهائي والمناسب في الواجهة
    with st.chat_message("assistant"):
        st.write(reply)

