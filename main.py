# السطر 1: استيراد المكتبات الأساسية لتشغيل واجهة التطبيق
import streamlit as st

# السطر 2: استيراد دالة البحث المحلية من مجلد المحرك (Engine)
from engine.inference import search_knowledge_base

# السطر 3: إعدادات الصفحة الافتراضية للتطبيق (العنوان والأيقونة)
st.set_page_config(page_title="Saeed LogiC Pro", page_icon="🚀", layout="centered")

# السطر 4: عرض العناوين الرئيسية لمساعد التسوق في أعلى الواجهة
st.title("Saeed LogiC Pro 🚀")
st.subheader("نظام محلي لدمج العروض والكوبونات")

# ----------------------------------------------------------------------
# السطر 5: [قسم قابل للتطوير] قاموس التحيات الذكي والردود المخصصة لها
# يمكنك إضافة أي تحية جديدة هنا مستقبلاً وسيتعرف عليها النظام تلقائياً
# ----------------------------------------------------------------------
GREETINGS_MAP = {
    ("السلام عليكم", "سلام", "الاسلام عليكم"): 
        "وعليكم السلام ورحمة الله وبركاته! أهلاً بك في مساعد التسوق الخاص بك. كيف يمكنني مساعدتك في العروض والكوبونات اليوم؟",
        
    ("صباح الخير", "صباح النور"): 
        "صباح النور والسرور! أهلاً بك في مساعد التسوق الخاص بك. كيف يمكنني مساعدتك في العروض والكوبونات اليوم؟",
        
    ("مساء الخير", "مساء النور"): 
        "مساء النور والسرور! أهلاً بك في مساعد التسوق الخاص بك. كيف يمكنني مساعدتك في العروض والكوبونات اليوم؟",
        
    ("مرحبا", "أهلا", "هلا", "اهلين"): 
        "أهلاً وسهلاً بك! يسعدني مساعدتك في العروض والكوبونات اليوم، تفضل بسؤالك."
}

# السطر 6: بناء شريط إدخال النص المخصص للمستخدم في أسفل الشاشة
user_input = st.chat_input("...اسألني عن العروض المتاحة")

# السطر 7: بدء معالجة البيانات بمجرد إرسال المستخدم للسؤال
if user_input:
    
    # السطر 8: عرض رسالة المستخدم الحالية في الواجهة
    with st.chat_message("user"):
        st.write(user_input)
        
    # السطر 9: فحص التحيات بشكل مرن وقابل للتطوير (Loop)
    reply = None
    for keywords, response_text in GREETINGS_MAP.items():
        if any(kw in user_input.strip() for kw in keywords):
            reply = response_text
            break # التوقف بمجرد إيجاد الرد المناسب لسرعة الأداء

    # السطر 10: إذا لم تكن المدخلات تحية، يتم التوجه فوراً لقاعدة البيانات JSON
    if reply is None:
        reply = search_knowledge_base(user_input) 
        
    # السطر 11: عرض الرد النهائي الذكي في واجهة المحادثة
    with st.chat_message("assistant"):
        st.write(reply)
